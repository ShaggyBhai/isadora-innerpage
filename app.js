function changeBgColor(color){
    const cont = document.querySelector('.overlay');
    cont.style.background = color;
}
function imgSlider(anything){
    document.querySelector('.bg').src=anything;
}
function boxSlider(color){
    const boxSlider = document.querySelector('.box');
    boxSlider.style.background = color;
}
function iconSlider(anything){
    document.querySelector('.menu').src=anything;
}